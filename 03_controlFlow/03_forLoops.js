//looping OOP feature which allows the execution of a code
//repeatedly without having to write the x amount of times.

//for loop - prints to console repeatedly
for (let i = 0; i < 5; i++) {
  console.log("Good Morning", i);
}

//loop  and display even numbers
for (let i = 0; i < 10; i++) {
  if (i % 2 === 0) console.log(i);
}

//loop  and display odd numbers
for (let i = 0; i < 10; i++) {
  if (i % 2 !== 0) console.log(i);
}

//loop  and reverse
for (let i = 10; i >= 1; i--) {
  console.log(i);
}
